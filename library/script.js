let addBtn=document.querySelector("#add");
addBtn.addEventListener("click",()=>{
    let title=document.querySelector("#title");
    let addTitle=title.value;
    let author=document.querySelector("#author");
    let addAuthor=author.value;
    let year=document.querySelector("#year");
    let addYear=year.value;
    let tbody=document.querySelector("tbody");
    let newtr=document.createElement('tr');
    tbody.appendChild(newtr);
    let td1=document.createElement('td');
    let td2=document.createElement('td');
    let td3=document.createElement('td');
    let td4=document.createElement('td');
    let deletebtn=document.createElement('button');
    deletebtn.classList.add("delete");
    deletebtn.innerText="Delete Book";
    let removebtn=document.createElement('button');
    removebtn.classList.add("remove");
    removebtn.innerText="Remove Book";
    let updatebtn=document.createElement('button');
    updatebtn.classList.add("update");
    updatebtn.innerText="Update Book";
    td1.innerText=addTitle;
    td2.innerText=addAuthor;
    td3.innerText=addYear;
    newtr.appendChild(td1);
    newtr.appendChild(td2);
    newtr.appendChild(td3); 
    newtr.appendChild(td4);
    td4.appendChild(deletebtn);
    td4.appendChild(removebtn); 
    td4.appendChild(updatebtn);
    deletebtn.addEventListener("click",()=>{
        td1.remove();
        td2.remove();
        td3.remove();
        td4.remove();
    });
    removebtn.addEventListener("click",()=>{
        td1.innerText=" ";
        td2.innerText=" ";
        td3.innerText=" ";
        title.value=" ";
        author.value=" ";
        year.value=" ";
    });
    updatebtn.addEventListener("click",()=>{
        let title=document.querySelector("#title");
        let addTitle=title.value;
        let author=document.querySelector("#author");
        let addAuthor=author.value;
        let year=document.querySelector("#year");
        let addYear=year.value;
        td1.innerText=addTitle;
        td2.innerText=addAuthor;
        td3.innerText=addYear;
    });
});
